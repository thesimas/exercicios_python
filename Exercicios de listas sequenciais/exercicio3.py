#Faça um algoritmo para calcular quantas ferraduras são necessárias para equipar todos os cavalos comprados para um haras.

quant_cavalos=input('Quantos cavalos você comprou?')
cavalos=int(quant_cavalos)*4
ferraduras=int(quant_cavalos)+int(cavalos)

print('Você irá necessitar de',ferraduras,'ferraduras para equipar seus cavalos.')
